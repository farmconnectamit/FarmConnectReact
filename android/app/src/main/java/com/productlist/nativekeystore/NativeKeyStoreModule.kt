        private const val KEY_ALIAS = "com.productlist.nativekeystore.KEY_ALIAS"
        private const val ANDROID_KEY_STORE = "AndroidKeyStore"
        private const val TRANSFORMATION = "AES/GCM/NoPadding"
        private const val ANDROID_API_M = 23
        private const val PASSWORD_ENC = "password_enc"
        private const val REFRESH_ENC = "refresh_token_enc"

