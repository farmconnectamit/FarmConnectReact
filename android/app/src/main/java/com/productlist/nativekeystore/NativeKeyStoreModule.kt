    override fun resetGenericPassword(promise: Promise) {
        try {
            sharedPrefs.edit().clear().apply()

            if (Build.VERSION.SDK_INT >= ANDROID_API_M) {
                val keyStore = KeyStore.getInstance(ANDROID_KEY_STORE)
                keyStore.load(null)
                keyStore.deleteEntry(KEY_ALIAS)
